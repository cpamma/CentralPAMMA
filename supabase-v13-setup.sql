-- Public read for active schedule rows so members can see schedule changes.
drop policy if exists "public read active schedule_overrides" on public.schedule_overrides;
create policy "public read active schedule_overrides"
  on public.schedule_overrides
  for select
  using (is_active = true);

drop policy if exists "public read active schedule_date_overrides" on public.schedule_date_overrides;
create policy "public read active schedule_date_overrides"
  on public.schedule_date_overrides
  for select
  using (is_active = true);

-- Member-only read for activity history.
drop policy if exists "member read own billing history" on public.member_billing_history;
create policy "member read own billing history"
  on public.member_billing_history
  for select
  using (lower(user_email) = lower(coalesce(auth.jwt() ->> 'email', '')));

drop policy if exists "member read own attendance history" on public.member_attendance_history;
create policy "member read own attendance history"
  on public.member_attendance_history
  for select
  using (lower(user_email) = lower(coalesce(auth.jwt() ->> 'email', '')));

