create table if not exists public.schedule_overrides (
  id bigint generated by default as identity primary key,
  day_key text not null,
  is_active boolean not null default true,
  is_closed boolean not null default false,
  message text,
  gym_hours text,
  classes_json jsonb,
  created_at timestamp with time zone not null default now()
);

create unique index if not exists schedule_overrides_day_key_unique
  on public.schedule_overrides (day_key)
  where is_active = true;

create table if not exists public.schedule_date_overrides (
  id bigint generated by default as identity primary key,
  date_iso text not null,
  is_active boolean not null default true,
  is_closed boolean not null default false,
  message text,
  gym_hours text,
  classes_json jsonb,
  created_by text,
  created_at timestamp with time zone not null default now()
);

alter table public.schedule_date_overrides add column if not exists created_by text;

create unique index if not exists schedule_date_overrides_date_unique
  on public.schedule_date_overrides (date_iso)
  where is_active = true;

create table if not exists public.member_billing_history (
  id bigint generated by default as identity primary key,
  user_email text not null,
  billed_at timestamp with time zone not null default now(),
  amount numeric(10,2) not null,
  status text not null default 'paid',
  method text,
  note text,
  created_at timestamp with time zone not null default now()
);

create index if not exists member_billing_history_user_email_idx
  on public.member_billing_history (lower(user_email), billed_at desc);

create table if not exists public.member_attendance_history (
  id bigint generated by default as identity primary key,
  user_email text not null,
  attended_at timestamp with time zone not null default now(),
  title text not null,
  coach text,
  note text,
  created_at timestamp with time zone not null default now()
);

create index if not exists member_attendance_history_user_email_idx
  on public.member_attendance_history (lower(user_email), attended_at desc);

